/**
 * ============================================================================
 *  ДЕСЯТИЧНОЕ ЧИСЛО  ->  РИМСКОЕ ЧИСЛО
 *  Decimal number    ->  Roman numeral
 * ============================================================================
 *
 *  ИДЕЯ (как лего):
 *  Представь, что у тебя есть кошелёк с "монетами". Но монеты тут не обычные —
 *  это римские числа: 1000(M), 900(CM), 500(D), 400(CD), 100(C), 90(XC),
 *  50(L), 40(XL), 10(X), 9(IX), 5(V), 4(IV), 1(I).
 *
 *  Чтобы "оплатить" число (например 2024), мы берём САМУЮ БОЛЬШУЮ монету,
 *  которая ещё помещается в оставшуюся сумму, и кладём её букву в ответ.
 *  Потом вычитаем её ценность из суммы и повторяем, пока сумма не станет 0.
 *
 *  Это и есть "жадный алгоритм" (greedy): всегда хватаем самое большое,
 *  что влезает. Для римских чисел он всегда даёт правильный ответ.
 * ============================================================================
 */

// ----------------------------------------------------------------------------
// ШАГ 1. "Кошелёк монет".
// Список пар: [ценность числом, как пишется буквами].
// ВАЖНО: отсортирован от БОЛЬШОГО к МАЛЕНЬКОМУ — алгоритм на это рассчитывает.
// Сюда специально добавлены "хитрые" монеты 900, 400, 90, 40, 9, 4 —
// потому что в римской системе их пишут вычитанием (IV = 5-1, IX = 10-1 и т.д.).
// ----------------------------------------------------------------------------
const ROMAN_COINS = [
    [1000, 'M'],
    [900, 'CM'],
    [500, 'D'],
    [400, 'CD'],
    [100, 'C'],
    [90, 'XC'],
    [50, 'L'],
    [40, 'XL'],
    [10, 'X'],
    [9, 'IX'],
    [5, 'V'],
    [4, 'IV'],
    [1, 'I'],
];

/**
 * Превращает десятичное число в римскую строку.
 *
 * @param {number} num — целое число от 1 до 3999 (классический римский диапазон).
 * @returns {string}    — строка римскими буквами, например 2024 -> "MMXXIV".
 */
function toRoman(num) {
    // --------------------------------------------------------------------
    // ШАГ 2. Проверка "а можно ли вообще это перевести?".
    // Римляне не знали ноль и не писали дроби/отрицательные.
    // Number.isInteger проверяет, что число ЦЕЛОЕ (не 3.5).
    // Если что-то не так — сразу кидаем ошибку, чтобы не считать мусор.
    // --------------------------------------------------------------------
    if (!Number.isInteger(num) || num < 1 || num > 3999) {
        throw new RangeError(
            `Ожидалось целое число от 1 до 3999, получено: ${num}`
        );
    }

    // --------------------------------------------------------------------
    // ШАГ 3. Готовим "пустой лист", куда будем дописывать буквы.
    // result — наш ответ, пока пустой.
    // remaining — сколько ещё осталось "оплатить". Сначала это всё число.
    // --------------------------------------------------------------------
    let result = '';
    let remaining = num;

    // --------------------------------------------------------------------
    // ШАГ 4. Идём по монетам от большой к маленькой (внешний цикл for...of).
    // На каждом шаге value = ценность монеты, symbol = её буквы.
    // --------------------------------------------------------------------
    for (const [value, symbol] of ROMAN_COINS) {
        // ----------------------------------------------------------------
        // ШАГ 5. Пока эта монета ВЛЕЗАЕТ в остаток — кладём её снова и снова.
        // Пример: остаток 2000, монета 1000 -> кладём M, остаток 1000,
        // она всё ещё влезает -> кладём ещё M, остаток 0 -> выходим.
        // Так "MM" получается автоматически.
        // ----------------------------------------------------------------
        while (remaining >= value) {
            result += symbol; // дописываем буквы монеты в ответ
            remaining -= value; // уменьшаем остаток на ценность монеты
        }

        // ----------------------------------------------------------------
        // ШАГ 6. Мелкая оптимизация: если уже всё оплатили (остаток 0),
        // нет смысла перебирать оставшиеся мелкие монеты — выходим.
        // ----------------------------------------------------------------
        if (remaining === 0) break;
    }

    // --------------------------------------------------------------------
    // ШАГ 7. Остаток всегда дойдёт до 0 (монета "1"/I добивает что угодно),
    // значит result полностью готов. Возвращаем его.
    // --------------------------------------------------------------------
    return result;
}

// ----------------------------------------------------------------------------
// ШАГ 8. Маленькая проверка прямо в файле (запусти: node roman-numerals.js).
// Каждая строка показывает: вход -> что получилось.
// ----------------------------------------------------------------------------
const samples = [1, 4, 9, 14, 40, 90, 400, 2024, 3888, 3999];
for (const n of samples) {
    console.log(`${n}\t-> ${toRoman(n)}`);
}

// ----------------------------------------------------------------------------
// ШАГ 9. Отдаём функцию наружу, чтобы другие файлы/тесты могли её использовать.
// (CommonJS — стиль, который понимает Node по умолчанию.)
// ----------------------------------------------------------------------------
module.exports = { toRoman };
