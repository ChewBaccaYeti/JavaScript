// =====================================================================
// Selection Sort / Сортировка выбором
// ---------------------------------------------------------------------
// Идея: ищем минимум в неотсортированной части и ставим его в начало.
//       Граница "отсортировано | не отсортировано" растёт слева направо.
// Time:  O(n²) во всех случаях
// Space: O(1)
// Stable: no (стандартная версия меняет порядок равных элементов)
// In-place: yes
// =====================================================================

function selectionSort(arr) {
    const n = arr.length;

    for (let i = 0; i < n - 1; i++) {
        // Индекс минимума в "хвосте" [i..n-1]
        let minIdx = i;

        for (let j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIdx]) {
                minIdx = j;
            }
        }

        // Меняем только если нашли что-то меньше (микро-оптимизация)
        if (minIdx !== i) {
            [arr[i], arr[minIdx]] = [arr[minIdx], arr[i]];
        }
    }

    return arr;
}

console.log(selectionSort([64, 25, 12, 22, 11])); // [11, 12, 22, 25, 64]

module.exports = selectionSort;
