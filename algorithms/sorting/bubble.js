// =====================================================================
// Bubble Sort / Сортировка пузырьком
// ---------------------------------------------------------------------
// Идея: на каждом проходе сравниваем соседние элементы и меняем местами,
//       если они в неправильном порядке. После i-го прохода i наибольших
//       элементов "всплыли" в конец массива.
// Time:  O(n²) worst / avg, O(n) best (уже отсортирован, с флагом swapped)
// Space: O(1)
// Stable: yes
// In-place: yes
// =====================================================================

/**
 * @param {number[]} arr - входной массив
 * @returns {number[]} - тот же массив, отсортированный по возрастанию
 */
function bubbleSort(arr) {
    const n = arr.length;

    // Внешний цикл: n-1 проходов достаточно
    for (let i = 0; i < n - 1; i++) {
        // Флаг: если за проход не было обменов — массив уже отсортирован
        let swapped = false;

        // Внутренний цикл: до n-1-i, потому что последние i элементов
        // уже на своих местах (после каждого прохода "всплывает" максимум)
        for (let j = 0; j < n - 1 - i; j++) {
            if (arr[j] > arr[j + 1]) {
                // Деструктуризация — идиоматичный JS swap (без temp переменной)
                [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]];
                swapped = true;
            }
        }

        // Ранний выход: оптимизация для почти отсортированных данных
        if (!swapped) break;
    }

    return arr;
}

// --- Пример ---
console.log(bubbleSort([5, 2, 9, 1, 5, 6])); // [1, 2, 5, 5, 6, 9]
console.log(bubbleSort([])); // []
console.log(bubbleSort([1])); // [1]
console.log(bubbleSort([3, 3, 3])); // [3, 3, 3]

module.exports = bubbleSort;
