// =====================================================================
// Insertion Sort / Сортировка вставками
// ---------------------------------------------------------------------
// Идея: как карты в руке — берём очередной элемент и вставляем в нужное
//       место в уже отсортированной левой части массива.
// Time:  O(n²) worst/avg, O(n) best (почти отсортированный)
// Space: O(1)
// Stable: yes
// In-place: yes
// Заметка: эффективен на маленьких/почти отсортированных массивах,
//          часто используется как подпрограмма в TimSort.
// =====================================================================

function insertionSort(arr) {
    const n = arr.length;

    // Начинаем с i=1: элемент arr[0] считаем уже "отсортированным"
    for (let i = 1; i < n; i++) {
        const current = arr[i]; // элемент, который вставляем
        let j = i - 1;

        // Сдвигаем элементы правее, пока не нашли позицию для current
        while (j >= 0 && arr[j] > current) {
            arr[j + 1] = arr[j];
            j--;
        }

        arr[j + 1] = current;
    }

    return arr;
}

console.log(insertionSort([5, 2, 4, 6, 1, 3])); // [1, 2, 3, 4, 5, 6]

module.exports = insertionSort;
